let numero = "1";

document.write(`<p>Valor da minha variável: ${numero = 1}</p>`);
// = atribuição

document.write(`<p>Valor da minha variável: ${numero == 1}</p>`);
// == comparação (valor)

document.write(`<p>Valor da minha variável: ${numero === 1}</p>`);
// === comparação (valor e tipo)

let marca = "Nike";
let resultado = marca !== 'Nike';

document.write(`<p>Marca: ${resultado}</p>`);
// !== diferente (valor e tipo)
